class PromptBuilder:
    """Prompt 构建器"""
    
    @staticmethod
    def build_qa_prompt(query: str, context: str) -> str:
        """构建基础问答 Prompt"""
        return QA_TEMPLATE.format(context=context, query=query)
    
    @staticmethod
    def build_qa_with_history_prompt(
        query: str, context: str, history: List[Dict]
    ) -> str:
        """构建带历史的问答 Prompt"""
        history_text = ""
        for msg in history:
            role = "用户" if msg["role"] == "user" else "助手"
            history_text += f"{role}：{msg['content']}\n"
        
        return QA_WITH_HISTORY_TEMPLATE.format(
            context=context,
            history=history_text if history_text else "（无历史对话）",
            query=query
        )
    
    @staticmethod
    def build_summary_prompt(context: str) -> str:
        """构建摘要 Prompt"""
        return SUMMARY_TEMPLATE.format(context=context)


# ============================================================================
# Token 估算工具
# ============================================================================

def estimate_tokens(text: str) -> int:
    """估算文本的 token 数
    
    粗略估算规则：
    - 中文字符约 1.5 token/字
    - 英文单词约 1.3 token/词
    - 标点符号和空白按 0.5 token/字符
    """
    cn_chars = len(re.findall(r'[\u4e00-\u9fff]', text))
    en_words = len(re.findall(r'[a-zA-Z]+', text))
    other_chars = len(re.findall(r'[^\u4e00-\u9fffa-zA-Z]+', text))
    return int(cn_chars * 1.5 + en_words * 1.3 + other_chars * 0.5)